package speed;

public class booleanType {

	public static void main(String[] args) {
		//boolean is either True or False
		
		boolean flag = true;
		System.out.println("The boolean type is " + flag +".");
		
		byte range;
		range = 124;

		System.out.println(range);
		
		short temperature;
		temperature = -204;
		
		System.out.println(temperature);
	}

}
